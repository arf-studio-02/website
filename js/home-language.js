/* =========================
   APPLY HOME LANGUAGE
========================= */

function applyHomeLanguage(){

    const lang =
    localStorage.getItem("language") || "id";

    if(!homeTranslations[lang]) return;

    document
    .querySelectorAll("[data-home]")
    .forEach(element => {

        const key =
        element.dataset.home;

        if(homeTranslations[lang]?.[key]){

            element.textContent =
            homeTranslations[lang][key];

        }

    });

}

